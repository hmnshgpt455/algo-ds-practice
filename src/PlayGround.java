import problems.books.elementsOfProgrammingInterviews.Helper;

public class PlayGround {
    public static void main(String[] args) {

        Helper.bin(8);
        System.out.println();
        Helper.bin(8 ^ 1);
        System.out.println();
        Helper.bin(8 & 1);
        System.out.println();
        Helper.bin(8 | 1);
    }
}
